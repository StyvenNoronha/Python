gameName = "World, of, Warcraft".upper()#deixa tudo maiúsculo
gameDescription = "MMORPG".lower()#deixa tudo minusculo

print(gameName.capitalize())#deixa a primeira letra maiúscula
print(gameDescription.title())#deixa a primeira letra de cada palavra maiúscula
print(gameName.center(30,"="))#centraliza a string)
print(gameName.count("W"))#conta quantas vezes a letra W aparece
print(gameName.find("W"))#mostra a posição da letra W
print(gameName.replace("W","M"))#substitui a letra W pela letra M
print(gameName.split(','))#separa a string em uma lista)
